package club.laky.sirius.admin.service.impl;

import club.laky.sirius.admin.entity.SysUser;
import club.laky.sirius.admin.dao.SysUserDao;
import club.laky.sirius.admin.service.SysUserService;
import org.springframework.stereotype.Service;

import javax.annotation.Resource;
import java.util.List;

/**
 * (SysUser)表服务实现类
 *
 * @author lakyjpan
 * @since 2021-04-06 22:57:57
 */
@Service("sysUserService")
public class SysUserServiceImpl implements SysUserService {
    @Resource
    private SysUserDao sysUserDao;

    /**
     * 通过ID查询单条数据
     *
     * @param id 主键
     * @return 实例对象
     */
    @Override
    public SysUser queryById(Integer id) {
        return this.sysUserDao.queryById(id);
    }

    /**
     * 查询多条数据
     *
     * @param offset 查询起始位置
     * @param limit  查询条数
     * @return 对象列表
     */
    @Override
    public List<SysUser> queryAllByLimit(int offset, int limit) {
        return this.sysUserDao.queryAllByLimit(offset, limit);
    }

    /**
     * 新增数据
     *
     * @param sysUser 实例对象
     * @return 实例对象
     */
    @Override
    public SysUser insert(SysUser sysUser) {
        this.sysUserDao.insert(sysUser);
        return sysUser;
    }

    /**
     * 修改数据
     *
     * @param sysUser 实例对象
     * @return 实例对象
     */
    @Override
    public SysUser update(SysUser sysUser) {
        this.sysUserDao.update(sysUser);
        return this.queryById(sysUser.getId());
    }

    /**
     * 通过主键删除数据
     *
     * @param id 主键
     * @return 是否成功
     */
    @Override
    public boolean deleteById(Integer id) {
        return this.sysUserDao.deleteById(id) > 0;
    }

    @Override
    public List<SysUser> queryAdminList(Integer offset, Integer limit, String nickname, Integer departmentId, Integer jobId, Integer state) {
        return sysUserDao.queryAdminList(offset, limit, nickname, departmentId, jobId, state);
    }

    @Override
    public int queryAdminListCount(String nickname, Integer departmentId, Integer jobId, Integer state) {
        return sysUserDao.queryAdminListCount(nickname, departmentId, jobId, state);
    }
}